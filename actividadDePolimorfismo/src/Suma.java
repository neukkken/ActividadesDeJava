public class Suma extends Calculadora{

    public int suma (int a, int b){
        return a+b;
    }

    public int suma (int a, int b, int c, int d){
        return a+b+c+d;
    }

    public float suma (float a, float b){
        return a+b;
    }

    public double suma (double a, double b){
        return a+b;
    }

}
